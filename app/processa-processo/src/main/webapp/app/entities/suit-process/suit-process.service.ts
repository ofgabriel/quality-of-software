import axios from 'axios';

import { ISuitProcess } from '@/shared/model/suit-process.model';

const baseApiUrl = 'api/suit-processes';

export default class SuitProcessService {
  public find(id: number): Promise<ISuitProcess> {
    return new Promise<ISuitProcess>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: ISuitProcess): Promise<ISuitProcess> {
    return new Promise<ISuitProcess>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
